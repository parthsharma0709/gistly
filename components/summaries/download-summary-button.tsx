'use client'

import { Download } from "lucide-react";
import { Button } from "../ui/button";


type DownloadSummaryButtonProps={
    title:string;
     fileName : string;
      summaryText :string;
      createdAt: string;
}


export default function DownloadSummaryButton({title,fileName,summaryText,createdAt}:DownloadSummaryButtonProps){

    const handleDownlaod=()=>{
        const summaryContent= `#${title}
Generate Summary
Generate on:${new Date(createdAt).toLocaleDateString()}
${summaryText}
Original FileName:${fileName}
Generated By Gistly`;

          const blob= new Blob([summaryContent],{type:'text/plain'});
          const url= URL.createObjectURL(blob);
          const link=document.createElement('a');
          link.href=url;
          link.download=`Summary-${title.replace(/[^-z0-9]/gi,'_')}.txt`;
          document.body.appendChild(link);
          link.click();
          document.body.removeChild(link);
          URL.revokeObjectURL(url);
       
    }
    return (
        <Button onClick={handleDownlaod} size={'sm'} className="h-8 px-3 bg-rose-100 text-rose-600 hover:text-rose-700 hover:bg-rose-50">
            <Download className="h-4 w-4 mr-1 "/>
            Download Summary
        </Button>
    )

}